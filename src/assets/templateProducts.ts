export const getProductsByTemplate = (template: TTemplate) => {
  switch (template) {
    case 'ELECTRONICS':
      return electronicsProducts
    case 'FASHION':
      return fashionProducts
    case 'FOOD':
      return foodProducts
    default:
      return defaultProducts
  }
}

export const getCategoriesByTemplate = (template: TTemplate) => {
  const products = getProductsByTemplate(template)
  const categories = [...new Set(products.map((p) => p.category))]
  return categories
}
